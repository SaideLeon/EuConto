    private fun createConta(codigo: String, titulo: String): ContaPGC {
        val classe = codigo.first().toString().toInt()
        var natureza = "ACTIVO"
        var typeCorrencia: String? = null

        when (classe) {
            1 -> {
                natureza = "ACTIVO"
                typeCorrencia = "CORRENTE"
            }
            2 -> {
                natureza = "ACTIVO"
                typeCorrencia = "CORRENTE"
            }
            3 -> {
                natureza = "ACTIVO"
                typeCorrencia = if (codigo == "3.5") "CORRENTE" else "NAO_CORRENTE"
            }
            4 -> {
                if (codigo.startsWith("4.1") || codigo.startsWith("4.5") || codigo.startsWith("4.7") || 
                    codigo.startsWith("4.9.3") || codigo.startsWith("4.9.4")) {
                    natureza = "ACTIVO"
                    typeCorrencia = "CORRENTE"
                } else if (codigo.startsWith("4.2") || codigo.startsWith("4.4") || codigo.startsWith("4.6") || 
                           codigo.startsWith("4.8") || codigo.startsWith("4.9.1") || codigo.startsWith("4.9.2") || 
                           codigo == "4.9") {
                    natureza = "PASSIVO"
                    typeCorrencia = "CORRENTE"
                } else if (codigo.startsWith("4.3")) {
                    natureza = "PASSIVO"
                    typeCorrencia = if (codigo == "4.3.1.2") "NAO_CORRENTE" else if (codigo == "4.3.1.1" || codigo == "4.3.1.3") "CORRENTE" else null
                }
            }
            5 -> {
                natureza = "CAPITAL_PROPRIO"
                typeCorrencia = null
            }
            6 -> {
                natureza = "GASTO"
                typeCorrencia = null
            }
            7 -> {
                natureza = "RENDIMENTO"
                typeCorrencia = null
            }
            8 -> {
                natureza = "RESULTADO"
                typeCorrencia = null
            }
        }
        return ContaPGC(codigo, titulo, classe, natureza, typeCorrencia)
    }
